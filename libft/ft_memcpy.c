#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*d;
	char	*e;

	if (n)
	{
		d = dst;
		e = (char*)src;
		while (n--)
		{
			*d++ = *e++;
		}
	}
	return (dst);
}
